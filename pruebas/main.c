#include "get_next_line.h"
#include <stdio.h>
#include <fcntl.h>

int main(void)
{
    int fd;
    char *line;

    // Test archivo normal
    printf("\n=== Test archivo normal ===\n");
    fd = open("test.txt", O_RDONLY);
    while ((line = get_next_line(fd)) != NULL)
    {
        printf("%s", line);
        free(line);
    }
    close(fd);

    // Test archivo vacío
    printf("\n=== Test archivo vacío ===\n");
    fd = open("empty.txt", O_RDONLY);
    while ((line = get_next_line(fd)) != NULL)
    {
        printf("%s", line);
        free(line);
    }
    close(fd);

    // Test archivo grande
    printf("\n=== Test archivo grande ===\n");
    fd = open("giant.txt", O_RDONLY);
    while ((line = get_next_line(fd)) != NULL)
    {
        printf("%s", line);
        free(line);
    }
    close(fd);

    return (0);
}
